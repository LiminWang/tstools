//=============================================================================
// Sub-function declare:
//=============================================================================
static int deal_with_parameter(int argc, char *argv[]);
static void show_help();

//=============================================================================
// The main function:
//=============================================================================
int main(int argc, char *argv[])
{
        char tbuf[1024 + 10]; // txt data buffer
        unsigned char bbuf[ 204 + 10]; // bin data buffer

        if(0 != deal_with_parameter(argc, argv))
        {
                return -1;
        }

        fd_o = fopen(file_o, "wb");
        if(NULL == fd_o)
        {
                DBG(ERR_FOPEN_FAILED);
                return -ERR_FOPEN_FAILED;
        }

        while(NULL != fgets(tbuf, 1000, stdin))
        {
                int b, t;

                //puts(tbuf);
                for(b = 0, t = 0; ; b++, t++)
                {
                        uint8_t h, l;

                        h = tbuf[t++];
                        if('\0' == h || 0x0a == h || 0x0d == h) break;

                        l = tbuf[t++];
                        if('\0' == l || 0x0a == l || 0x0d == l) break;

                        bbuf[b] = t2b_table_h[h] | t2b_table_l[l];
                        //printf("%02X ", (int)bbuf[b]);
                }
                fwrite(bbuf, b, 1, fd_o);
        }

        fclose(fd_o);

        return 0;
}

//=============================================================================
// Subfunctions definition:
//=============================================================================
static int deal_with_parameter(int argc, char *argv[])
{
        int i;

        if(1 == argc)
        {
                // no parameter
                fprintf(stderr, "No binary file to write...\n\n");
                show_help();
                return -1;
        }

        for(i = 1; i < argc; i++)
        {
                if ('-' == argv[i][0])
                {
                        if (0 == strcmp(argv[i], "--help"))
                        {
                                show_help();
                                return -1;
                        }
                        else
                        {
                                fprintf(stderr, "Wrong parameter: %s\n", argv[i]);
                                DBG(ERR_BAD_ARG);
                                return -ERR_BAD_ARG;
                        }
                }
                else
                {
                        strcpy(file_o, argv[i]);
                }
        }

        return 0;
}

static void show_help()
{
        puts("Usage: tobin [options] bin_file [options]");
        puts("Options:");
        puts("  --help         Display this information");
        puts("");
        fprintf(stdout, "tobin v1.00 by ZHOU Cheng, %s %s\n", __TIME__, __DATE__);
        return;
}

