#include <stdio.h>
#include <stdlib.h>
#include <stdint.h>

#include "crc.h"

//=============================================================================
// enum & struct definition:
//=============================================================================

//=============================================================================
// Variables definition:
//=============================================================================
static uint8_t buf08[] =
{
        //00    01    02    03    04    05    06    07
        //0x00, 0x00, 0x00, 0x00
        0x05, 0x56, 0x67, 0x72
};

static uint8_t buf32[] =
{
        //00    01    02    03    04    05    06    07
        //0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        //0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0xB0, 0x11, 0x00, 0x00, 0xC1, 0x00, 0x00,
        0x00, 0x01, 0xE1, 0x00, 0x00, 0x00, 0xE0, 0x1F,
        0xE9, 0x62, 0xE7, 0x63
        
};

//=============================================================================
// Sub-function declare:
//=============================================================================

//=============================================================================
// The main function:
//=============================================================================
int main()
{
        crc_init();
        printf("CRC8  is 0x%02lX\n", CRC(buf08, 0x04,  8));
        printf("CRC32 is 0x%08lX\n", CRC(buf32, 0x10, 32));
        printf("crc32 is 0x%08lX\n", crc32(buf32, 0x10));
        exit(0);
}

